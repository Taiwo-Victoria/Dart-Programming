void main() {
  // All data types are objects making the default value null


  //Numbers without decimal point: int

  int score = 40;

  //int can also takes an hexadecimal number: 0x

  int hexvalue = 0x25DEA81;

  // Numbers with decimal point: double

  double percentage = 80.45;

  //double can also be used to save exponential value: e

  double expo = 34.9e10;

  // Letters: Strings

  String name = 'Victoria';

  // True/False: Boolean (bool)

  bool isAlive = true;

  // Automatic knows the data type: var

  var isValid = false;

  print(score);
  print(percentage);
  print(name);
  print(isAlive);
  print(isValid);
  print(hexvalue);
  print(expo);
}
