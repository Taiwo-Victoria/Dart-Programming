void main() {

  //Similer to if else if ladder statement.
  
  //Applicable for only int and string

  String grade = '14';

  switch (grade) {
    case 'A':
      print("Excellent !");
      break;

    case 'B':
      print('Very Good !');
      break;

    case 'C':
      print('Good !');
      break;

    case 'F':
      print('You have failed !');
      break;

    default:
      print('Ivalid !!!');
  }
}
